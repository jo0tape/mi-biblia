export function getDailyVerseRef(): DailyVerseRef {
  const today = new Date().toISOString().slice(0, 10).replace(/-/g, "");
  const seed = parseInt(today, 10);
  const idx = seed % DAILY_VERSE_REFS.length;
  return DAILY_VERSE_REFS[idx];
}
